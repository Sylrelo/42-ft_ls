#include "../includes/libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int dest;
	int sign;

	i = 0;
	dest = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		dest = dest * 10 + (str[i] - '0');
		i++;
	}
	return (dest * sign);
}
